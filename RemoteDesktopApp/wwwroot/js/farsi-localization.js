// Function to apply Farsi texts
function applyFarsiLocalization() {
    // Apply to common elements
    document.querySelectorAll('[data-farsi]').forEach(element => {
        const key = element.getAttribute('data-farsi');
        if (FarsiTexts[key]) {
            if (element.tagName === 'INPUT' && (element.type === 'submit' || element.type === 'button')) {
                element.value = FarsiTexts[key];
            } else if (element.tagName === 'INPUT' && element.placeholder !== undefined) {
                element.placeholder = FarsiTexts[key];
            } else {
                element.textContent = FarsiTexts[key];
            }
        }
    });
    
    // Apply to specific elements by ID or class
    const elementMappings = {
        // Dashboard
        'welcomeMessage': 'welcome_back',
        'todayEvents': 'today_events',
        'totalSpreadsheets': 'total_spreadsheets',
        'codeProjects': 'code_projects',
        'unreadMessages': 'unread_messages',
        'phoneNumber': 'your_phone_number',
        'missedCalls': 'missed_calls',
        'unreadSms': 'unread_sms',
        
        // Mobile Phone
        'dialerTab': 'dialer',
        'contactsTab': 'contacts',
        'smsTab': 'sms',
        'historyTab': 'history',
        'callBtn': 'call',
        'answerBtn': 'answer',
        'declineBtn': 'decline',
        'endCallBtn': 'end_call',
        
        // Forms
        'username': 'username',
        'password': 'password',
        'rememberMe': 'remember_me',
        'loginBtn': 'sign_in'
    };
    
    Object.keys(elementMappings).forEach(elementId => {
        const element = document.getElementById(elementId);
        const textKey = elementMappings[elementId];
        if (element && FarsiTexts[textKey]) {
            if (element.tagName === 'INPUT') {
                if (element.type === 'submit' || element.type === 'button') {
                    element.value = FarsiTexts[textKey];
                } else {
                    element.placeholder = FarsiTexts[textKey];
                }
            } else {
                element.textContent = FarsiTexts[textKey];
            }
        }
    });
}

// Function to get Farsi text
function getFarsiText(key) {
    return FarsiTexts[key] || key;
}

// Apply localization when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
    applyFarsiLocalization();
});

// Export for use in other scripts
window.FarsiTexts = FarsiTexts;
window.getFarsiText = getFarsiText;
window.applyFarsiLocalization = applyFarsiLocalization;
